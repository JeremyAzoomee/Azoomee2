/**
 * Holds all config info for the settings.
 * The instance is created and then the properties are set.
 */

AZM.Overlay.Config = function()
{
    this.showHelp = false;
    this.showReplay = false;
    this.showHome = false;
    this.pausedTitle = false; // whether to show 'paused' as the title, rather than settings
};
